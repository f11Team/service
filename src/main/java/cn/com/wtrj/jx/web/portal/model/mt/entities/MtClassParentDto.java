package cn.com.wtrj.jx.web.portal.model.mt.entities;

public class MtClassParentDto {
	private Integer classNo;
	
	private String schoolCode;
	
	private Integer stage;
	
	private Integer grade;
	
	private String studentName;
	
	private String studentIdCardNo;
	
	private String parentName;
	
	private String parentPhone;
	
	private String className;

	public Integer getClassNo() {
		return classNo;
	}

	public void setClassNo(Integer classNo) {
		this.classNo = classNo;
	}

	public String getSchoolCode() {
		return schoolCode;
	}

	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}

	public Integer getStage() {
		return stage;
	}

	public void setStage(Integer stage) {
		this.stage = stage;
	}

	public Integer getGrade() {
		return grade;
	}

	public void setGrade(Integer grade) {
		this.grade = grade;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getStudentIdCardNo() {
		return studentIdCardNo;
	}

	public void setStudentIdCardNo(String studentIdCardNo) {
		this.studentIdCardNo = studentIdCardNo;
	}

	public String getParentName() {
		return parentName;
	}

	public void setParentName(String parentName) {
		this.parentName = parentName;
	}

	public String getParentPhone() {
		return parentPhone;
	}

	public void setParentPhone(String parentPhone) {
		this.parentPhone = parentPhone;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}
}
